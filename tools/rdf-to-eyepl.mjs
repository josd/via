#!/usr/bin/env node
import fs from 'node:fs/promises';
import process from 'node:process';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { parseNQuads, quadToEyepl } from './rdf-codec.mjs';
import { parseRdf } from './rdf-input.mjs';

export function compileRdfToEyepl(rdf, { scope = 'input', rules = '', includeSource = false } = {}) {
  return compileQuadsToEyepl(parseNQuads(rdf, { scope }), { rules, includeSource });
}
export async function compileRdfDocumentToEyepl(rdf, { scope = 'input', rules = '', includeSource = false, inputPath, contentType, baseIRI } = {}) {
  const quads = await parseRdf(rdf, { scope, path: inputPath, contentType, baseIRI });
  return compileQuadsToEyepl(quads, { rules, includeSource });
}
export function compileQuadsToEyepl(quads, { rules = '', includeSource = false } = {}) {
  const predicate = includeSource ? 'source_rdf' : 'rdf';
  const facts = quads.map((q) => quadToEyepl(q, predicate));
  const parts = ['% Generated by tools/rdf-to-eyepl.mjs.', 'query(rdf(S, P, O, G)).'];
  if (includeSource) parts.push('rdf(S, P, O, G) :- source_rdf(S, P, O, G).');
  if (facts.length) parts.push(facts.join('\n'));
  if (String(rules).trim()) parts.push(`% Rules\n${String(rules).trim()}`);
  return `${parts.join('\n\n')}\n`;
}
async function main(argv) {
  let input = '-'; let output = '-'; let rulesFile; let scope; let format; let baseIRI; let includeSource = false;
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '-h' || a === '--help') return usage();
    if (a === '--include-source') includeSource = true;
    else if (a === '--rules') rulesFile = required(argv, ++i, a);
    else if (a === '--scope') scope = required(argv, ++i, a);
    else if (a === '--format') format = required(argv, ++i, a);
    else if (a === '--base') baseIRI = required(argv, ++i, a);
    else if (a === '-o' || a === '--output') output = required(argv, ++i, a);
    else if (a.startsWith('-') && a !== '-') throw new Error(`unknown option: ${a}`);
    else if (input === '-') input = a;
    else throw new Error(`unexpected argument: ${a}`);
  }
  const rdf = await read(input);
  const rules = rulesFile ? await fs.readFile(rulesFile, 'utf8') : '';
  const result = await compileRdfDocumentToEyepl(rdf, {
    scope: scope ?? (input === '-' ? 'stdin' : path.basename(input)), rules, includeSource,
    inputPath: input === '-' ? undefined : input,
    contentType: format,
    baseIRI: baseIRI ?? (input === '-' ? undefined : pathToFileURL(path.resolve(input)).href),
  });
  if (output === '-') process.stdout.write(result); else await fs.writeFile(output, result);
}
function usage() { process.stdout.write('Usage: node tools/rdf-to-eyepl.mjs [options] [input|-]\n\n  --format TYPE      RDF media type or extension (required for stdin)\n  --base IRI         Base IRI for relative references\n  --rules FILE       Append Eyepl rules using rdf/4\n  --include-source   Emit source and derived quads\n  --scope NAME       Set blank-node scope\n  -o, --output FILE  Write program to FILE\n'); }
function required(a, i, o) { if (a[i] == null) throw new Error(`${o} requires a value`); return a[i]; }
function read(f) { if (f !== '-') return fs.readFile(f, 'utf8'); return new Promise((resolve, reject) => { let s = ''; process.stdin.setEncoding('utf8'); process.stdin.on('data', (c) => { s += c; }); process.stdin.on('end', () => resolve(s)); process.stdin.on('error', reject); }); }
if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) main(process.argv.slice(2)).catch((e) => { process.stderr.write(`rdf-to-eyepl: ${e.message}\n`); process.exitCode = 1; });
